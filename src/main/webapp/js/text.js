function Letter(char, x, y, w, h) {
    var pts = letters[char];
    this.x = x;
    this.y = y;
    this.w = w;
    this.h = h;
    
    this.draw = function(g) {
        for (var i = 0; i < pts.length; i += 4) {
            g.moveTo(pts[i]*this.w/2+this.x, -pts[i+1]*this.h/2+this.y);
            g.lineTo(pts[i+2]*this.w/2+this.x, -pts[i+3]*this.h/2+this.y);
        }
    };
}