function Letter(char, x, y, w, h) {
    var pts = letters[char];
    this.x = x;
    this.y = y;
    this.w = w;
    this.h = h;
    
    this.draw = function(g) {
        g.moveTo(pts[0]*this.w/2+this.x, -pts[1]*this.h/2+this.y);
        for (var i = 0; i < pts.length; i += 2) {
            g.lineTo(pts[i]*this.w/2+this.x, -pts[i+1]*this.h/2+this.y);
        }
    };
}